     * Create a resource
     *
     * @param  mixed $data
     * @return ApiProblem|mixed
     */
    public function create($data)
    {

        $authEntity = new AuthEntity();
        $authEntity->email = $data->email;
        $authEntity->password = $data->password;

        $result = $this->mapper->auth($authEntity);

        if( $result  ) {
            return $result;
